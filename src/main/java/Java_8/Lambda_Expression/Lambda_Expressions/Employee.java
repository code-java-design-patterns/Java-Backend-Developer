package src.main.java.Java_8.Lambda_Expression.Lambda_Expressions;

public class Employee {
    private int age;
    private String name;

    public Employee() {
        this.age = age;
        this.name = name;
    }

    public void print() {
        System.out.println("Age: " + age);
        System.out.println("Name: " + name);
    }

}
